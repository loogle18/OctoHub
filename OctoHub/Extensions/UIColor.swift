import UIKit

extension UIColor {
    class var customRed: UIColor {
        return UIColor(red: 255/255, green: 59/255, blue: 48/255, alpha: 1.0)
    }
    
    class var customGreen: UIColor {
        return UIColor(red: 76/255, green: 217/255, blue: 100/255, alpha: 1.0)
    }
    
    class var customBlue: UIColor {
        return UIColor(red: 0/255, green: 122/255, blue: 255/255, alpha: 1.0)
    }
    
    class var customDarkGray: UIColor {
        return UIColor(red: 142/255, green: 142/255, blue: 147/255, alpha: 1.0)
    }
    
    class var customGray: UIColor {
        return UIColor(red: 229/255, green: 229/255, blue: 229/255, alpha: 1.0)
    }
    
    class var customLightGray: UIColor {
        return UIColor(red: 251/255, green: 251/255, blue: 251/255, alpha: 1.0)
    }
}
